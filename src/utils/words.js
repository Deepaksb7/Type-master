const ALPHABET = "abcdefghijklmnopqrstuvwxyz".split("");

// Synthesizes a random word of length n using only unlocked letters.
function synthesizeWord(n, lockedSet) {
  const pool = ALPHABET.filter((c) => !lockedSet.has(c));
  if (pool.length === 0) return "";
  let word = "";
  for (let i = 0; i < n; i++) {
    word += pool[Math.floor(Math.random() * pool.length)];
  }
  return word;
}

// Generates a block of text consisting of `count` words.
// Only words that do NOT contain any letter from the locked set are used.
// If no real word qualifies, a synthesized word is used.
export function generateText(count, locked = new Set()) {
  const lcLocked = new Set(Array.from(locked).map((c) => c.toLowerCase()));

  // pool of words having 4-7 letters and not containing any locked letter
  let pool = WORDS.filter((w) => {
    const lw = w.toLowerCase();
    if (lw.length < 4 || lw.length > 7) return false;
    for (const ch of lw) {
      if (lcLocked.has(ch)) return false;
    }
    return true;
  });

  const result = [];
  for (let i = 0; i < count; i++) {
    if (pool.length > 0) {
      result.push(pool[Math.floor(Math.random() * pool.length)]);
    } else {
      // Synthesize a word if no qualifying word exists
      const n = 4 + Math.floor(Math.random() * 4); // word length between 4 and 7
      const word = synthesizeWord(n, lcLocked);
      if (word !== "") result.push(word);
    }
  }
  return result.join(" ");
}
